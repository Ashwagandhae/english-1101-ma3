	<li>
		<h2>How can I find out if someone I know qualifies for food assistance through the ACFB?</h2>
		The goal of the ACFB is to assist anyone in need, and this is reflected in the conduct of meal distribution.
		Food pantries are located around the Atlanta area and open to anyone in need (some may require proof
		of local residency). For additional information, and to find the nearest food pantry, use
		<a href="https://www.acfb.org/get-help/food-map/#googtrans(en%7Cen)">this website </a>.
		Additionally, you can make an appointment with a community food center (locations in Jonesboro,
		Marietta, and Stone Mountain) using
		<a href="https://www.acfb.org/get-help/food-map/#googtrans(en%7Cen)">this website</a>. The ACFB
		can also help connect you with resources like SNAP and Medicaid, assuming you meet requirements
		for those programs.
	</li>
	<li>
		<h2>Are there any volunteer opportunities specifically for children or families?</h2>
		Yes! Children as young as 8 can volunteer (must be accompanied by an adult chaperone), and it is
		a fantastic way to make an impact in the community and raise awareness at an early age. For more
		information, please visit
		<a href="https://www.acfb.org/volunteer/">this website</a>.
	</li>

	<li>
		<h2>How does the ACFB ensure the food it distributes is safe and nutritious?</h2>
		The ACFB follows strict guidelines to ensure the safety of the food it distributes. By working with
		local food manufacturers, grocers, and farmers, they work to ensure all food meets safety standards.
		All goods are checked for expiration date, packaging integrity, and nutritional content. Volunteers
		and employees are trained in safe handling procedures to avoid contamination. Any perishable foods
		are distributed quickly so they donâ€™t spoil.
	</li>

	<li>
		<h2>
			Can I donate my time or skills in ways other than volunteering, such as offering professional
			services (e.g., legal or financial expertise)?
		</h2>
		Absolutely! The ACFB welcomes donations of professional services, ranging from marketing, administrative,
		financial, and legal professionals, in a volunteer/pro bono capacity in order to help the organization
		improve its operations, therefore increasing its effectiveness at addressing hunger. Please contact
		the ACFB directly by emailing info@acfb.org or calling 404-892-3333.
	</li>

	<li>
		<h2>How can I get my business involved with the Atlanta Food Bank?</h2>
		There are many ways to get your business involved! Consider organizing a food drive among employees,
		making a corporate donation, or sponsoring local events, to invest in a hunger-free community. For
		more information, visit
		<a href="https://www.acfb.org/corporate-partnership/">this website</a>.
	</li>
</ol>
